import UIKit

extension UIColor {
    
    class func baseColor() -> UIColor {
        return UIColor(red: 0.290, green: 0.812, blue: 0.584, alpha: 1.000)
    }
    
    class func lowColor() -> UIColor {
        return UIColor(red: 0.206, green: 0.582, blue: 0.418, alpha: 1.000)
    }
    
}